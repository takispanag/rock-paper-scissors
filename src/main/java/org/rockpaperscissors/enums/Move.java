package org.rockpaperscissors.enums;

/**
 * Enum representing the possible moves in the Rock-Paper-Scissors game.
 */
public enum Move {
    /**
     * The "Rock" move.
     */
    ROCK,

    /**
     * The "Paper" move.
     */
    PAPER,

    /**
     * The "Scissors" move.
     */
    SCISSORS
}