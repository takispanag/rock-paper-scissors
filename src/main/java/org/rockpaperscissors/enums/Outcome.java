package org.rockpaperscissors.enums;

/**
 * Enum representing the possible outcomes of a round in the Rock-Paper-Scissors game.
 */
public enum Outcome {
    /**
     * Outcome indicating that Player A wins the round.
     */
    PLAYER_A_WINS,

    /**
     * Outcome indicating that Player B wins the round.
     */
    PLAYER_B_WINS,

    /**
     * Outcome indicating a tie in the round.
     */
    TIE
}